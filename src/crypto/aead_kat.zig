//! Known-answer tests for the typed AEAD wrapper.
//!
//! These tests exercise the public `AeadAlg` variants through `Aead(alg)` and
//! compare byte-exact outputs against published vectors.
const std = @import("std");
const aead_mod = @import("aead.zig");

const testing = std.testing;

const ChaCha = aead_mod.Aead(.chacha20_poly1305);
const Aes256Gcm = aead_mod.Aead(.aes256_gcm);

fn expectTamperFails(
    comptime A: type,
    box: *const A,
    nonce: A.Nonce,
    aad: []const u8,
    ciphertext: []const u8,
    tag: A.Tag,
    comptime plaintext_len: usize,
) !void {
    var out: [plaintext_len]u8 = undefined;

    var bad_tag = tag;
    bad_tag[0] ^= 0x01;
    try testing.expectError(
        aead_mod.Error.AuthFailed,
        box.open(nonce, aad, ciphertext, bad_tag, out[0..]),
    );

    var bad_ciphertext: [plaintext_len]u8 = undefined;
    @memcpy(bad_ciphertext[0..], ciphertext);
    bad_ciphertext[0] ^= 0x01;
    try testing.expectError(
        aead_mod.Error.AuthFailed,
        box.open(nonce, aad, bad_ciphertext[0..], tag, out[0..]),
    );
}

